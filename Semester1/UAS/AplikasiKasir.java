    private void CNamaBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CNamaBarangActionPerformed
        if (CNamaBarang.getSelectedIndex() > 0) {
            THarga.setText(String.valueOf(hargaBarang[CNamaBarang.getSelectedIndex() - 1]));
            TJumlah.setText("1");
            TSubtotal.setText(String.valueOf(hargaBarang[CNamaBarang
                        .getSelectedIndex() - 1] * Integer.parseInt(TJumlah.getText())));
        } else {
            THarga.setText(null);
            TJumlah.setText(null);
        }
    }//GEN-LAST:event_CNamaBarangActionPerformed

    private void TJumlahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TJumlahKeyReleased
        try {
            TSubtotal.setText(String.valueOf(hargaBarang[CNamaBarang
                    .getSelectedIndex() - 1] * Integer.parseInt(TJumlah.getText())));
        } catch (NumberFormatException e) {
            TSubtotal.setText(null);
        }
    }//GEN-LAST:event_TJumlahKeyReleased

    private void BTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTambahActionPerformed
        int harga, jumlah, subtotal;
        
        if (CNamaBarang.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Masukkan nama barang", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (TJumlah.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah barang", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            harga = Integer.parseInt(THarga.getText());
            jumlah = Integer.parseInt(TJumlah.getText());
            subtotal = Integer.parseInt(TSubtotal.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukan harus berupa angka", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
            
        tbl.addRow(new Object[]{namaBarang[CNamaBarang.getSelectedIndex()], harga, jumlah, subtotal});
        TBLRincian.setModel(tbl);
        buatTotalan();
    }//GEN-LAST:event_BTambahActionPerformed

    private void BHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BHapusActionPerformed
        while (TBLRincian.getSelectedRow() != -1) {
            baris = TBLRincian.getSelectedRow();
            tbl.removeRow(baris);
        }
        buatTotalan();
    }//GEN-LAST:event_BHapusActionPerformed

    private void BResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BResetActionPerformed
        CNamaBarang.setSelectedIndex(0);
        THarga.setText(null);
        TJumlah.setText(null);
        TSubtotal.setText(null);
        tbl.getDataVector().removeAllElements();
        tbl.fireTableDataChanged();
        TTotal.setText(null);
        TDiskon.setText(null);
        TBayar.setText(null);
    }//GEN-LAST:event_BResetActionPerformed

