    public void buatTotalan() {
        int total = 0, diskon = 0, bayar;
        for (int i = 0; i < TBLRincian.getRowCount(); i++) {
            total += (int) TBLRincian.getValueAt(i, 3);
        }
        if (total > 100000) diskon = total * 5 / 100;
        bayar = total - diskon;
        TTotal.setText(String.valueOf(total));
        TDiskon.setText(String.valueOf(diskon));
        TBayar.setText(String.valueOf(bayar));
    }

